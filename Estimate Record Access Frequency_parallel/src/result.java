package parallelForward;

public class result {
	private int key;
	private float estdVale;
	public result(int key, float estdVale) {
		super();
		this.key = key;
		this.estdVale = estdVale;
	}
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public float getEstdVale() {
		return estdVale;
	}
	public void setEstdVale(float estdVale) {
		this.estdVale = estdVale;
	}
	
}
